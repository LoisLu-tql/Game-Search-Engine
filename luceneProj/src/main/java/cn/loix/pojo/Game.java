package cn.loix.pojo;

public class Game {
    private Integer id;
    private String game_src;
    private String game_name;
    private String game_link;
    private String game_img;
    private String game_intro;
    private String game_birth;
    private String game_developer;
    private String game_publisher;
    private String game_tag;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getGame_src() {
        return game_src;
    }

    public void setGame_src(String game_src) {
        this.game_src = game_src;
    }

    public String getGame_name() {
        return game_name;
    }

    public void setGame_name(String game_name) {
        this.game_name = game_name;
    }

    public String getGame_link() {
        return game_link;
    }

    public void setGame_link(String game_link) {
        this.game_link = game_link;
    }

    public String getGame_img() {
        return game_img;
    }

    public void setGame_img(String game_img) {
        this.game_img = game_img;
    }

    public String getGame_intro() {
        return game_intro;
    }

    public void setGame_intro(String game_intro) {
        this.game_intro = game_intro;
    }

    public String getGame_birth() {
        return game_birth;
    }

    public void setGame_birth(String game_birth) {
        this.game_birth = game_birth;
    }

    public String getGame_developer() {
        return game_developer;
    }

    public void setGame_developer(String game_developer) {
        this.game_developer = game_developer;
    }

    public String getGame_publisher() {
        return game_publisher;
    }

    public void setGame_publisher(String game_publisher) {
        this.game_publisher = game_publisher;
    }

    public String getGame_tag() {
        return game_tag;
    }

    public void setGame_tag(String game_tag) {
        this.game_tag = game_tag;
    }
}

